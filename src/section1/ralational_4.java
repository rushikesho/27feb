package section1;

public class ralational_4 {
	public static void main(String[] args) {
		
	//relational operators only gives output in boolean (true or false)
		
   int rushi = 120 ;
   int rathod = 120 ;
   boolean r ;
   
   r = rushi == rathod ;
   System.out.println(r);
   
   r = rushi > rathod ;
   System.out.println(r);
   
   r = rushi >= rathod ;
   System.out.println(r);
   
   r = rushi < rathod ;
   System.out.println(r);
   
   r = rushi <= rathod ;
   System.out.println(r);
   
   r = rushi != rathod ;
   System.out.println(r);
   
}
}